package underclass.pigs;
/**
 * 黑猪类
 * @author 寜
 * */
public class BlackPig extends pig{
    public BlackPig(String color,int weight) {
        super(color,weight);
    }
    /**颜色获取*/
    public String getColor() {
        return super.getColor();
    }
    /**体重获取*/
    public int attack() {
        return super.getWeight();
    }
}
